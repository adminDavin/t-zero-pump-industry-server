package com.t.zero.b.i.pump.db.dao.filters;

public class PumpInformationDataFilters {
	private String pumpSource;

	private String pumpName;
	
	private String language;
	
	public String getPumpSource() {
		return pumpSource;
	}

	public void setPumpSource(String pumpSource) {
		this.pumpSource = pumpSource;
	}

	public String getPumpName() {
		return pumpName;
	}

	public void setPumpName(String pumpName) {
		this.pumpName = pumpName;
	}

	public String getLanguage() {
		return language;
	}

	public void setLanguage(String language) {
		this.language = language;
	}
}
